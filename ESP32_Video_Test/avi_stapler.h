void add_frame(File &file, uint8_t *buf, size_t len) {
  // A tiny header that tells the player: "Here comes a single photo of this exact size"
  uint8_t frame_header[8] = {0x30, 0x30, 0x64, 0x63, 0x00, 0x00, 0x00, 0x00};
  
  // AVI requires all photos to have an even number of bytes. We add a blank padding byte if it's odd.
  uint8_t padding = len % 2;
  uint32_t frame_size = len + padding;
  
  // Stamp the size into the frame header
  frame_header[4] = frame_size & 0xFF;
  frame_header[5] = (frame_size >> 8) & 0xFF;
  frame_header[6] = (frame_size >> 16) & 0xFF;
  frame_header[7] = (frame_size >> 24) & 0xFF;

  // Write the header, the picture, and the padding
  file.write(frame_header, 8);
  file.write(buf, len);
  if (padding) {
    uint8_t pad_byte = 0x00;
    file.write(&pad_byte, 1);
  }
  
  // Update our running tally of the total file size
  movi_size += 8 + len + padding;
}

void end_avi(File &file, int frames) {
  // Calculate total file size for the cover page
  uint32_t file_size = movi_size + 324 - 8;
  
  // Seek backward to update the total size
  file.seek(4);
  file.write((uint8_t*)&file_size, 4);

  // Seek backward to update the total frame count in two required places
  file.seek(48);
  file.write((uint8_t*)&frames, 4);
  file.seek(140);
  file.write((uint8_t*)&frames, 4);

  // Seek backward to update the size of the video data part
  file.seek(320);
  file.write((uint8_t*)&movi_size, 4);
  
  file.close();
}